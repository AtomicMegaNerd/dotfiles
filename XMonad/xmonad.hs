main = do
  xmproc <- spawnPipe "xmobar /home/cdunphy/.config/xmobar/xmobar.cfg"
  xmonad $ docks defaults {
    logHook = dynamicLogWithPP $ xmobarPP {
        ppOutput = \x -> hPutStrLn xmproc x
        , ppCurrent = xmobarColor "#c3e88d" "" . wrap "[" "]" -- Current workspace in xmobar
        , ppVisible = xmobarColor "#999999" ""                -- Visible but not current workspace
        , ppHidden = xmobarColor "#77a88d" "" . wrap "*" ""   -- Hidden workspaces in xmobar
        , ppHiddenNoWindows = xmobarColor "#77a88d" ""        -- Hidden workspaces (no windows)
        , ppTitle = xmobarColor "#d0d0d0" "" . shorten 60     -- Title of active window in xmobar
        , ppSep =  "<fc=#666666> | </fc>"                     -- Separators in xmobar
    }
  }

